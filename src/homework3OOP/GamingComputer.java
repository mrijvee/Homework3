package homework3OOP;

public class GamingComputer {

	private String computerName;
	private String computerPrice;
	private String monitorPrice;

	public String getComputerName() {
		return computerName;
	}
	public void setComputerName(String computerName) {
		this.computerName = computerName;
	}
	public String getComputerPrice() {
		return computerPrice;
	}
	public void setComputerPrice(String computerPrice) {
		this.computerPrice = computerPrice;
	}
	public String getMonitorPrice() {
		return monitorPrice;
	}
	public void setMonitorPrice(String monitorPrice) {
		this.monitorPrice = monitorPrice;
	}

}
